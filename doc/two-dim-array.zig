    const dataSingleChar: [16]u8 = FONT[pc];
    for (0..16) |y| {
        //1. SoC crash
        std.debug.print("{d}", .{dataSingleChar[y]});
        //2. works
        std.debug.print("{d}", .{FONT[pc][y]});
    }
}

